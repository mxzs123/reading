export type TranslationKey = keyof (typeof messages)["zh-CN"];
export type TFunction = (key: TranslationKey, values?: TranslationValues) => string;

export function isLocale(value: string | null | undefined): value is Locale {
  return LOCALES.includes(value as Locale);
}

export function formatMessage(template: string, values?: TranslationValues): string {
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (match, key) => {
    const value = values[key];
    return value === undefined || value === null ? match : String(value);
  });
}

export function translate(
  locale: Locale,
  key: TranslationKey,
  values?: TranslationValues
): string {
  return formatMessage(messages[locale][key] ?? messages["zh-CN"][key] ?? key, values);
}
